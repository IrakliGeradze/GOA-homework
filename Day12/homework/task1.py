# > (მეტია)
print(5 > 3)      # true
print(10 > 10)    # false
print(-2 > -5)    # true
print(7 > 9)      # false
print(0 > -1)     # true

# >= (მეტია ან ტოლია)
print(5 >= 3)     # true
print(10 >= 10)   # true
print(-2 >= -1)   # false
print(7 >= 8)     # false
print(0 >= 0)     # true

# < (ნაკლებია)
print(5 < 3)      # false
print(10 < 11)    # true
print(-2 < -1)    # true
print(7 < 7)      # false
print(0 < -1)     # false

# <= (ნაკლებია ან ტოლია)
print(5 <= 3)     # false
print(10 <= 11)   # True
print(-2 <= -2)   # True
print(7 <= 6)     # False
print(0 <= 0)     # True

# == (ტოლია)
print(5 == 5)     # True
print(10 == 9)    # False
print(-2 == -2)   # True
print(7 == 8)     # False
print(0 == 0)     # True

# != (არ არის ტოლი)
print(5 != 3)     # True
print(10 != 10)   # False
print(-2 != -3)   # True
print(7 != 7)     # False
print(0 != 1)     # True

